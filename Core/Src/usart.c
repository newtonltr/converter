uint8_t serial_unpack_recieve_data(struct serial_t *serial, uint8_t *pData, uint16_t len, struct converter_protocol *protocol)
{
  struct can_pack_protocol_head *head = (struct can_pack_protocol_head *)pData;
  uint16_t crc = 0;
  
  memcpy(&crc, pData + sizeof(struct can_pack_protocol_head) + head->len, 2);
  if (checkRxCRC(pData, sizeof(struct can_pack_protocol_head) + head->len, crc)) {
    memcpy(&protocol->header, pData, sizeof(struct can_pack_protocol_head));
    memcpy(&protocol->data, pData + sizeof(struct can_pack_protocol_head), head->len);
    protocol->crc = crc;
    return 1;
  }
  return 0;
}
uint8_t serial_get_data(struct serial_t *serial, struct converter_protocol *protocol)
{
  uint8_t ret = 0;

  if (serial->rx_flag) {
    serial->rx_flag = 0;
    HAL_UART_DMAStop(serial->huart);
    if (serial_unpack_recieve_data(serial, serial->rx_buffer, serial->size, protocol)) {
      ret = 1;
    } else {
      ret = 0;
    }
    memset(serial->rx_buffer, 0, SERIAL_RX_BUFFER_SIZE);
    HAL_UARTEx_ReceiveToIdle_DMA(serial->huart, serial->rx_buffer, SERIAL_RX_BUFFER_SIZE);
  }
  return ret;
}
void serial_data_packet(struct converter_protocol *protocol, struct fdcan_tx_frame *tx_frame)
{
  tx_frame->header.Identifier = protocol->header.id;
  tx_frame->header.IdType = protocol->header.id<=0x7ff?FDCAN_STANDARD_ID:FDCAN_EXTENDED_ID;
  tx_frame->header.TxFrameType = protocol->header.frame_type == CAN_DATA_FRAME?FDCAN_DATA_FRAME:FDCAN_REMOTE_FRAME;
  tx_frame->header.DataLength = (uint32_t)protocol->header.len;
  tx_frame->header.ErrorStateIndicator = FDCAN_ESI_ACTIVE;
  tx_frame->header.BitRateSwitch = FDCAN_BRS_OFF;
  tx_frame->header.FDFormat = FDCAN_CLASSIC_CAN;
  tx_frame->header.TxEventFifoControl = FDCAN_NO_TX_EVENTS;
  tx_frame->header.MessageMarker = 0;

  memcpy(tx_frame->data, protocol->data, protocol->header.len);
}

